#include "libft.h"

void		ft_putchar(wchar_t c)
{
	unsigned char	ch[4];

	if (c <= 127 || MB_CUR_MAX != 4)
		ch[0] = c;
	else if (c <= 2047)
	{
		ch[0] = (c >> 6 & 31) | 192;
		ch[1] = (c & 63) | 128;
	}
	else if (c <= 65535)
	{
		ch[0] = (c >> 12 & 15) | 224;
		ch[1] = (c >> 6 & 63) | 128;
		ch[2] = (c & 63) | 128;
	}
	else
	{
		ch[0] = (c >> 18 & 7) | 240;
		ch[1] = (c >> 12 & 63) | 128;
		ch[2] = (c >> 6 & 63) | 128;
		ch[3] = (c & 63) | 128;
	}
	write(1, &ch, size_of(c));
}
